public class bitWiseOp1 {
    public static void main(String args[]){
        System.out.println("using AND '&' operator " + (0&0));
        System.out.println("using OR '|' operator " + (5|6));
        System.out.println("using XOR '^' operator " + (5^6));
        System.out.println("using One' compliement(NOT) '~' operator " + (~-5));
        System.out.println("using Left Shift '<<' operator " + (5<<2));
        System.out.println("using Rigth Shift '>>' operator " + (6>>1));
    }
}
